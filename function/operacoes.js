function soma (a,b){
    return a+b 
  }
  function sub (a,b){
    return a-b
  }
  function multi (a,b){
    return a*b
  }
  function div (a,b) {
    return a/b 
  }
/* Essas são as definições das funções soma, sub, multi e div.
Cada uma delas recebe dois parâmetros (a e b) e executa uma operação matemática entre esses valores, retornando o resultado.*/

  let Resultadodiv = div (10,5)
  let Resultadomulti = multi (10,5)
  let Resultadosub = sub (10,5)
  let Resultadosoma = soma (10,5)
  /*Aqui, cada função é chamada com argumentos específicos (no caso, 10 e 5) e os resultados dessas operações são armazenados em variáveis correspondentes (Resultadodiv, Resultadomulti, Resultadosub e Resultadosoma).*/
  
  alert (Resultadosoma)
  alert (Resultadosub)
  alert (Resultadomulti)
  alert (Resultadodiv)
  /*Por fim, cada um dos resultados das operações é exibido individualmente em um alert.*/